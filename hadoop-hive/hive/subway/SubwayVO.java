package vo;

public class SubwayVO {
	String Line;
	int Time;
	int Ride;
	int Takeoff;
	
	public String getLine() {
		return Line;
	}
	public void setLine(String line) {
		Line = line;
	}
	public int getTime() {
		return Time;
	}
	public void setTime(int time) {
		Time = time;
	}
	public int getRide() {
		return Ride;
	}
	public void setRide(int ride) {
		Ride = ride;
	}
	public int getTakeoff() {
		return Takeoff;
	}
	public void setTakeoff(int takeoff) {
		Takeoff = takeoff;
	}
}
