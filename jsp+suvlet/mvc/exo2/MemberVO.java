package model.vo;

public class MemberVO {
	String name;
	String number;
	String id;
	String pw;
	public String getName() {
		return name;
	}
	public String getNumber() {
		return number;
	}
	public String getId() {
		return id;
	}
	public String getPw() {
		return pw;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
}
